CREATE TABLE user_t (
    USERID VARCHAR2(20) PRIMARY KEY,
    USERNAME VARCHAR2(20) NOT NULL,
    PASSWORD VARCHAR2(20) NOT NULL,
    GENDER CHAR(1) CHECK(GENDER IN ('m', 'f')),
    EMAIL VARCHAR2(30),
    PHONE VARCHAR2(20),
    ADDRESS VARCHAR2(40) NOT NULL,
    REGISTERDATE DATE DEFAULT SYSDATE  
);

CREATE TABLE product_t (
    PID VARCHAR2(30) PRIMARY KEY,
    CATEGORY VARCHAR2(20) NOT NULL,
    PRODUCTNAME VARCHAR2(50) NOT NULL,
    EXPLANATION VARCHAR2(2048),
    PRICE NUMBER(30) NOT NULL,
    REGISTERDATE DATE DEFAULT SYSDATE,
    INVENTORY NUMBER(20) NOT NULL,
    ISBASKET CHAR(1) CHECK(ISBASKET IN ('y', 'n'))
);

CREATE TABLE order_t(
	oid	VARCHAR2(40),
	productname VARCHAR2(50),
	qty NUMBER(30),
	totalprice NUMBER(38),
	userid VARCHAR2(20),
	address VARCHAR2(40),
	phone VARCHAR2(20),
	orderstat VARCHAR2(20),
	orderdate DATE DEFAULT SYSDATE,
	ordermemo VARCHAR2(1024),
	paymentmethod VARCHAR2(20),
	approved_at DATE
);